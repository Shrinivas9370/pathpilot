export const getCareerById = (id) => CAREERS.find(c => c.id === id);

export const matchCareers = (userProfile) => {
  const { interests = [], skills = {}, personality = [] } = userProfile;

  return CAREERS.map(career => {
    // Interest score: how many user interests match career interests
    const interestMatches = career.interests.filter(i =>
      interests.some(ui => ui.toLowerCase().includes(i) || i.includes(ui.toLowerCase()))
    ).length;
    const interestScore = Math.min((interestMatches / Math.max(career.interests.length, 1)) * 100, 100);

    // Skill score: based on user's skill level average
    const skillValues = Object.values(skills);
    const avgSkill = skillValues.length > 0 ? skillValues.reduce((a, b) => a + b, 0) / skillValues.length : 50;
    const skillScore = avgSkill;

    // Personality score
    const personalityMatches = career.personality.filter(p =>
      personality.some(up => up.toLowerCase().includes(p) || p.includes(up.toLowerCase()))
    ).length;
    const personalityScore = Math.min((personalityMatches / Math.max(career.personality.length, 1)) * 100, 100);

    const finalScore = Math.round((interestScore * 0.4) + (skillScore * 0.4) + (personalityScore * 0.2));

    return { ...career, matchScore: finalScore };
  }).sort((a, b) => b.matchScore - a.matchScore);
};
